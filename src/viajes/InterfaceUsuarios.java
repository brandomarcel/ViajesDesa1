    public void ingreso (){
        String sql;
        sql ="SELECT * FROM USUARIOS";
        try {
            Statement psd = cn.createStatement();
            ResultSet rs = psd.executeQuery(sql);
            
            String usuario = jtxtUsuario.getText();
            String clave = jtxtContrasena.getText();
            
            
            
            while (rs.next()) {
                String UsuarioRs=rs.getString("USU_CEDULA");
                String ClaveRs = rs.getString("USU_CLAVE");
                //Esto es un if condicional
                if ( (usuario == null ? UsuarioRs == null :usuario.equals(UsuarioRs)) && (clave == null ? ClaveRs == null : clave.equals(ClaveRs))) {
                    if ("Administrador".equals(rs.getString("USU_PERFIL"))) {
                        InterfacePrincipal pr = new InterfacePrincipal();
                    pr.setVisible(true);
                    this.dispose();
                    
                    }else if ("Secretaria".equals(rs.getString("USU_PERFIL"))) {
                        InterfacePrincipal pr = new InterfacePrincipal();
                    pr.setVisible(true);
                   pr.jmenuReportes.setEnabled(false);
                    this.dispose();
                    }
                    
                    
                    
                }else{
                    jlblmensaje.setVisible(true);
                }
                
            }
        
        } catch (SQLException ex) {

            JOptionPane.showMessageDialog(null, ex+"No ingreso nada");
            //Logger.getLogger(InterfaceUsuarios.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
